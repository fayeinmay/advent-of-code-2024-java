    private static long puzzleTwo(Map<Integer, List<Character>> map2D) {
        long total = 0;

        int px2 = 0;
        int py2 = 0;

        while (py2 < map2D.size() && px2 < map2D.get(py2).size()) {
            Character character = map2D.get(py2).get(px2);
            if (character.equals('A') && masAsX(map2D, px2, py2)) {
                total++;
            }
            if (px2 + 1 < map2D.get(py2).size()) {
                px2++;
            } else {
                px2 = 0;
                py2++;
            }
        }


        return total;
    }

    public static boolean masAsX(Map<Integer, List<Character>> map2D, int nextX, int nextY) {
        int crosses = 0;
        if (checkRightUp(map2D, nextX, nextY, 'S') && checkLeftDown(map2D, nextX, nextY, 'M')) {
            crosses++;
        }

        if (checkRightUp(map2D, nextX, nextY, 'M') && checkLeftDown(map2D, nextX, nextY, 'S')) {
            crosses++;
        }

        if (checkRightDown(map2D, nextX, nextY, 'S') && checkLeftUp(map2D, nextX, nextY, 'M')) {
            crosses++;
        }

        if (checkRightDown(map2D, nextX, nextY, 'M') && checkLeftUp(map2D, nextX, nextY, 'S')) {
            crosses++;
        }

        return crosses == 2;
    }

    public static boolean checkRightDown(Map<Integer, List<Character>> map2D, int px, int py, char letter) {
        return px + 1 < map2D.get(py).size() && py + 1 < map2D.size() && map2D.get(py + 1).get(px + 1) == letter;
    }

    public static boolean checkLeftDown(Map<Integer, List<Character>> map2D, int px, int py, char letter) {
        return px - 1 >= 0 && py + 1 < map2D.size() && map2D.get(py + 1).get(px - 1) == letter;
    }

    public static boolean checkLeftUp(Map<Integer, List<Character>> map2D, int px, int py, char letter) {
        return px - 1 >= 0 && py - 1 >= 0 && map2D.get(py - 1).get(px - 1) == letter;
    }

    public static boolean checkRightUp(Map<Integer, List<Character>> map2D, int px, int py, char letter) {
        return px + 1 < map2D.get(py).size() && py - 1 >= 0 && map2D.get(py - 1).get(px + 1) == letter;
    }

    // Helper search function
    private static boolean searchFrom(int startX, int startY, int dx, int dy, String word, Map<Integer, List<Character>> map2D) {
        for (int k = 0; k < word.length(); k++) {
            int x = startX + k * dx;
            int y = startY + k * dy;

            // Check boundaries
            if (!map2D.containsKey(y) || x < 0 || x >= map2D.get(y).size() || map2D.get(y).get(x) != word.charAt(k)) {
                return false;
            }
        }
        return true;
    }

