    public static String getSolution() {
        long result;

        // Create grid
        Map<Integer, List<Character>> map2D = new HashMap<>();

        int row = 0;
        int col = 0;
        for (int i = 0; i < XMAS.length(); i++) {
            char currentChar = XMAS.charAt(i);

            if (currentChar != '\n') {
                map2D.putIfAbsent(row, new ArrayList<>());
                map2D.get(row).add(col, currentChar);
                col++;

            }

            if (col == 140) {
                row++;
                col = 0;
            }
        }

        result = puzzleOne(map2D);
        result = puzzleTwo(map2D);

        return Long.toString(result);
    }

    private static long puzzleOne(Map<Integer, List<Character>> map2D) {
        long total = 0;

        // Search for word in grid
        String word = "XMAS";
        for (int y = 0; y < map2D.size(); y++) {
            for (int x = 0; x < map2D.get(y).size(); x++) {
                for (int[] dir : DIRECTIONS) {
                    int dx = dir[0];
                    int dy = dir[1];
                    if (searchFrom(x, y, dx, dy, word, map2D)) {
                        total++;
                    }
                }
            }
        }

        return total;
    }

